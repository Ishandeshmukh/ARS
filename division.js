function increaseValueM() {
    var value = parseInt(document.getElementById('numberM').value, 10);
    value = isNaN(value) ? 0 : value;
    value++;
    document.getElementById('numberM').value = value;
}
  
  function decreaseValueM() {
    var value = parseInt(document.getElementById('numberM').value, 10);
    value = isNaN(value) ? 0 : value;
    value < 1 ? value = 1 : '';
    value--;
    document.getElementById('numberM').value = value;
}

